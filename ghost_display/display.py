console = Console()

def make_panel(ghost, datetime_str):
    text = Text()
    text.append(ghost, style="bold white")
    text.append("\n")
    text.append(f"🕓 {datetime_str}", style="bold green")
    return Align.center(Panel(text, title="👻 h-SHELL", border_style="bold cyan", padding=(1, 4)))

# Display ghost animation
with Live(console=console, refresh_per_second=4, screen=True) as live:
    while True:
        for frame in ghost_frames:
            now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
            panel = make_panel(frame, now)
            live.update(panel)
            sleep(0.8)

